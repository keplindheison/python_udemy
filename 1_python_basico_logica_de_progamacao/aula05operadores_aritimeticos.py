"""
+, -, *, /, //, **, %, ()

"""

print('Adição + ', 10 + 10)  # Adição
print('Subtração - ', 10 - 10)  # Subtração
print('Multiplicação * ', 10 * 10)  # Multiplicação
print('Divisão / ', 10 / 10)  # Divisão
print('Divisão Inteira // ', 10 // 10)  #Divisão Inteira
print('Exponênciação ** ', 10 ** 10)  # Exponênciação
print('Modulo (resto da divisão) % ', 10 % 10)  # Modulo